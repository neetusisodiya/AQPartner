package com.app.oooelePartner.Bean;

public class BeanWorkingRadius {
    private String id;
    private String expert_id;
    private String lat;
    private String lng;
    private String working_radius;
    private String address;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getExpert_id() {
        return expert_id;
    }

    public void setExpert_id(String expert_id) {
        this.expert_id = expert_id;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getWorking_radius() {
        return working_radius;
    }

    public void setWorking_radius(String working_radius) {
        this.working_radius = working_radius;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
